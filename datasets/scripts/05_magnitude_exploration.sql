/*
===============================================================================
Magnitude Analysis
===============================================================================
Purpose:
    - To quantify data and group results by specific dimensions.
    - For understanding data distribution across categories.

SQL Functions Used:
    - Aggregate Functions: SUM(), COUNT(), AVG()
    - GROUP BY, ORDER BY
===============================================================================
*/
SELECT 
  country,
  COUNT(customer_key) AS Total_customers
FROM gold.dim_customers
GROUP BY country
ORDER BY Total_customers DESC;

-- Find the total customers by gender
SELECT 
  gender,
  COUNT(customer_key) AS Total_customers
FROM gold.dim_customers
GROUP BY gender
ORDER BY Total_customers DESC;

-- Find the total products by categories
SELECT 
  category,
  COUNT(subcategory) AS Total_products
FROM gold.dim_products
GROUP BY category
ORDER BY Total_products DESC;

-- What is the average cost by category?
SELECT 
  category,
  AVG(cost) AS Total_cost
FROM gold.dim_products
GROUP BY category
ORDER BY Total_cost DESC;

-- Find the total revenue generated by each category?
SELECT 
  p.category,
  SUM(f.sales_amount) AS Total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_products p
ON p.product_key = f.product_key
GROUP BY category
ORDER BY Total_revenue DESC;

-- what is the total revenue generated by each customers

SELECT 
  c.customer_key,
  c.first_name,
  c.last_name,
  SUM(f.sales_amount) AS total_revenue
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c
ON c.customer_key = f.customer_key
GROUP BY
  c.customer_key,
  c.first_name,
  c.last_name
ORDER BY total_revenue DESC;

-- What is the distribution of sold items across countries?
SELECT 
  c.country,
  SUM(quantity) AS total_old_items
FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c
ON f.customer_key = c.customer_key
GROUP BY country
ORDER BY total_old_items DESC;


-- Find the total  cost by each category?
SELECT 
  p.category,
  SUM(p.cost) AS Total_cost
FROM gold.fact_sales f
LEFT JOIN gold.dim_products p
ON p.product_key = f.product_key
GROUP BY category
ORDER BY Total_cost DESC
